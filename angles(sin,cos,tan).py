import math
angle = float(input('Insert the desired angle: '))
print(f'The sine of {angle:.2f}° in radian is {math.sin(math.radians(angle)):.2f}')
print(f'The cosine of {angle:.2f}° in radian is {math.cos(math.radians(angle)):.2f}')
print(f'The tangent of {angle:.2f}° in radian is {math.tan(math.radians(angle)):.2f}')
