import math
angle = float(input('Insert the desired angle: '))
print('The sine of {:.2f}° in radian is {:.2f}'.format(angle, math.sin(math.radians(angle))))
print('The cosine of {:.2f}° in radian is {:.2f}'.format(angle, math.cos(math.radians(angle))))
print('The tangent of {:.2f}° in radian is {:.2f}'.format(angle, math.tan(math.radians(angle))))
